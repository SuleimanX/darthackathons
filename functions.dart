//Adding two numbers
int addTwo(num1, num2){
  return num1 + num2;
}

//Subtracting two numbers
int subtractTwo(num3, num4){
  return num3 - num4;
}


void main(){
  int result_addTwo = addTwo(10, 20);
  print("The sum of 10 and 20 is $result_addTwo");

  int result_subtractTwo = subtractTwo(70, 40);
  print("The subtraction of 70 and 40 is $result_subtractTwo");

  

}